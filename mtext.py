def 生成斐波那契数列(n):
  """
  生成斐波那契数列的前 N 个数。

  参数：
    n: 生成数列的长度。

  返回值：
    一个包含 N 个斐波那契数的列表。
  """

  if n <= 0:
    raise ValueError("n 必须大于 0")

  # 初始化前两个数
  a, b = 0, 1

  # 生成数列
  斐波那契数列 = []
  for _ in range(n):
    斐波那契数列.append(a)
    a, b = b, a + b

  return 斐波那契数列

# 示例用法
n = 10
斐波那契数列 = 生成斐波那契数列(n)
print(f"前 {n} 个斐波那契数：{斐波那契数列}")
